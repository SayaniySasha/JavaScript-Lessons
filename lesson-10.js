// Функции в js являются обьектами, мы можем передавать функции в функции в качестве аргументов, а также возвращать функции из функции
// Передача Функции в качестве аргументов может использоватся для реализации так называемых callback, тоесть для передачи Функции которая будет выполнена после выполнения Функции в которую она передается 
var x = function(callback){
	var text ="JAVASCRIPT"; 	
	callback(text);
};
x(function(t){
console.log("hello " + t);
});


// Функция может быть возвращаемым значением 
var z = function(){
	return function(){
		console.log("hi");
	}
};
// теперь когда мы вызываем эту Функцию в точку вызова возвращается анонимная Функция .
z()();


