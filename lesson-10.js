// function - ключевое слово 
// индентификатор - имя Функции 
// аргументы - это переменные какие мы передаем в нашу Функцию.
// внутри Функции мы можем что-то делать с этими переменными, и после этого мы можем вернуть с Функции какие-то значение
// для этого предусмотрено ключевое слово return

function fnc(name){
	return "hello "+name;
}

console.log(fnc("Sasha"));	
// для того чтобы вызвать эту функцию , используем выражение вызова, какое имеет такой синтаксис 

// выражение(аргументы);

// При этом в точку вызова , тоесть там где  мы прописали это выражение вернется значение которое мы возращаем из нашей Функции.
// в том случаи когда функция ничего не возвращает то она возвращает undefined

// По скольку мы знаем что функция возвращает строку , мы можем сразу вызвать методы для объекта String
console.log(fnc("sasha").toUpperCase());


// функцию можно также определить если присвоить какие-то переменной выражение определения Функции

